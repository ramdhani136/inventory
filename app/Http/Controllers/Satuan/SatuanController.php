<?php

namespace App\Http\Controllers\Satuan;

use App\Http\Controllers\Controller;
use App\Http\Resources\SatuanResource;
use App\Models\Satuan\Satuan;
use Illuminate\Http\Request;
use Symfony\Component\HttpFoundation\Response;

class SatuanController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        return SatuanResource::collection(Satuan::orderBy('nama', 'ASC')->get());
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        //
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $data = Satuan::create($request->all());
        return response(new SatuanResource($data), response::HTTP_CREATED);
    }
    

    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        return SatuanResource::collection(Satuan::where('id', $id)->get());
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        //
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        Satuan::where('id', $id)->update($request->all());
        return response('update', response::HTTP_CREATED);
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        Satuan::where('id', $id)->delete();
        return response('deleted', response::HTTP_OK);
    }
}
